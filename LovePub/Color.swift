import Foundation
import UIKit

extension UIColor {
    class var piBlack: UIColor {
        return UIColor(red: 74.0 / 255.0, green: 74.0 / 255.0, blue: 74.0 / 255.0, alpha: 1.0)
    }
    class var piSilver: UIColor {
        return UIColor(red: 233.0 / 255.0, green: 234.0 / 255.0, blue: 234.0 / 255.0, alpha: 1)
    }
    class var piRed: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 59.0 / 255.0, blue: 48.0 / 255.0, alpha: 1)
    }
}
